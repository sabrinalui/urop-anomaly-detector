if __name__=='__main__':

	# initialize params in seconds
	t0 = 7200
	t1 = 1800
	if len(sys.argv) == 3:
		t0 = int(sys.argv[1])
		t1 = int(sys.argv[2])

	listener = StreamingAnomalyHandler()
